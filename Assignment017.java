public class Assignment017 {
    public static void main(String[] args) {
        
        int a = new Integer(34);

        byte b = (byte)a;
        System.out.println("Integer instance to byte : "+b);

        short c = (short)a;
        System.out.println("Integer instance to short : "+c);

        long d = (long)a;
        System.out.println("Integer instance to long : "+d);

        float e = (float)a;
        System.out.println("Integer instance to float : "+e);

        double f = (double)a;
        System.out.println("Integer instance to double : "+f);
    }
}
